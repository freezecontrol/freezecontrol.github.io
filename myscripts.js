function openForm() {
  document.getElementById("myForm").style.display = "block";
}

function closeForm() {
  document.getElementById("myForm").style.display = "none";
}

function changeservice() {
  var x = document.getElementById("mailservice").value;
  if (x == "Air-Condition") {
	  document.getElementById("acdrop").style.display = "block";
	  document.getElementById("wmdrop").style.display = "none";
	  document.getElementById("rfdrop").style.display = "none";
	  console.log("1");
  }
  if (x == "Washing-Machine") {
	  document.getElementById("wmdrop").style.display = "block";
	  document.getElementById("acdrop").style.display = "none";
	  document.getElementById("rfdrop").style.display = "none";
	  console.log("2");
  }
  if (x == "Refrigerator") {
	  document.getElementById("rfdrop").style.display = "block";
	  document.getElementById("acdrop").style.display = "none";
	  document.getElementById("wmdrop").style.display = "none";
	  console.log("3");
  }
}
